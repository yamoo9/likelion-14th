// --------------------------------------------------------------------------
// 화살표 함수 표현식 (Arrow Function Expression)
// --------------------------------------------------------------------------

// JavaScript 함수를 만드는 방법 3가지

// 1. 함수 선언
function showMeTheMoney() { console.log('show me the money') }

showMeTheMoney()

// 2. 함수 표현식
let lookAndMe = function() { console.log('look and me') }

lookAndMe()

// 3. 화살표 함수 표현식
let readABook = () => { console.log('read a book') }

readABook()